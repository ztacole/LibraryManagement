        String password = tbPassword.getText();
        
        Petugas petugas = dao.findPetugas(username, password);
        if (petugas == null) {
            JOptionPane.showMessageDialog(null, "Username dan password salah");
            return;
        }
        Util.petugas = petugas;
        new DataBuku().setVisible(true);
